package test;

public class Switch 
{  
public static void main(String[] args) 
{  
    int number=20;  
    switch(number)
    {  
    case 10:
    	
    	System.out.println("10"); 
    	break; // optional
    	
    case 20:
    	
    	System.out.println("20");  
    	break;  // optional
    case 30:
    	
    	System.out.println("30");  
    	break;  // optional
    
    default:
    	
    	System.out.println("Not in 10, 20 or 30");  
    }  
} 

}  